const defaultLanguage = "it";

function getLocale() {
	return localStorage.getItem("locale") == undefined ? defaultLanguage : localStorage.getItem("locale");
}

function translateString(translationKey) {
	const language = localStorage.getItem("locale") == undefined ? defaultLanguage : localStorage.getItem("locale");
	return translations[translationKey][language];
}

function translatePage(language) {
	$("[data-translate-key]").each(function () {
		var $element = $(this);
		var translationKey = $element.data("translate-key");
		var translation = translations[translationKey] && translations[translationKey][language];
		$element.text(translation || translationKey);
	});
}

$(document).ready(function () {
	setLanguage();
	$("#language-it").click(function () {
		setLanguage("it");
	});
	$("#language-en").click(function () {
		setLanguage("en");
	});
});

function setLanguage(language) {
	if(language == undefined) {
		language = localStorage.getItem("locale") == undefined ? defaultLanguage : localStorage.getItem("locale");
	}
	localStorage.setItem("locale", language);
	translatePage(language);
	$("#selectedLanguage").text(language.toUpperCase());
	$(".dropdown-language").removeClass("active");
	$("#language-"+language).addClass("active");
	$("html").attr("lang", language);
}

